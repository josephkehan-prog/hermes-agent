def setup_tor():
    _p("\n═══════════════════════════════════════════════════", BOLD)
    _p("  OnionClaw Setup — Step 2: Tor ControlPort", BOLD)
    _p("═══════════════════════════════════════════════════", BOLD)
    _p("""
renew_identity() needs a Tor ControlPort to rotate circuits.
Without it, Tor keeps the same identity for the entire session.

Required torrc lines:
  ControlPort 9051
  CookieAuthentication 1
""")

    # Check if tor is installed
    tor_bin = shutil.which("tor")
    if not tor_bin:
        _p("⚠  Tor binary not found in PATH.", YELLOW)
        _p("   Install:  sudo apt install tor   (Debian/Ubuntu)", YELLOW)
        _p("             brew install tor        (macOS)", YELLOW)
        _p("             pacman -S tor           (Arch)", YELLOW)
    else:
        _p(f"✓ Tor found: {tor_bin}", GREEN)

    # Try to patch system torrc
    patched_system = False
    for torrc_path in TORRC_CANDIDATE_PATHS:
        if os.path.exists(torrc_path):
            if _already_has_controlport(torrc_path):
                _p(f"\n✓ ControlPort already configured in {torrc_path}", GREEN)
                patched_system = True
                break
            if _yes(f"\nPatch {torrc_path} to add ControlPort settings? (requires write access)"):
                try:
                    with open(torrc_path, "r") as f:
                        existing = f.read()
                    with open(torrc_path, "a") as f:
                        f.write(f"\n{TORRC_SNIPPET}")
                    _p(f"✓ Patched {torrc_path}", GREEN)
                    patched_system = True
                    break
                except PermissionError:
                    _p(f"  Permission denied — try:  sudo python3 setup.py", YELLOW)
                except Exception as e:
                    _p(f"  Failed: {e}", RED)

    if not patched_system:
        # Fall back: write custom torrc + instruct user to start tor with it
        _write_custom_torrc()

    # Print instructions regardless
    _p("""
─────────────────────────────────────────────────────────
  AFTER configuring torrc, restart Tor:

    sudo systemctl restart tor        # systemd (Debian/Ubuntu)
    sudo service tor restart          # SysV init
    brew services restart tor         # macOS Homebrew
    tor -f /tmp/tor_data/torrc &      # custom torrc (no root needed)

─────────────────────────────────────────────────────────
""", CYAN)

    # Verify ControlPort is reachable now
    _verify_controlport()


def _already_has_controlport(path: str) -> bool:
    try:
        with open(path, "r") as f:
            content = f.read()
        return any(
            line.strip().lower().startswith("controlport")
            for line in content.splitlines()
            if not line.strip().startswith("#")
        )
    except Exception:
        return False


def _write_custom_torrc():
    _p(f"\nWriting custom torrc → {CUSTOM_TORRC_PATH}", CYAN)
    try:
        os.makedirs(CUSTOM_TORRC_DIR, exist_ok=True)
        if os.path.exists(CUSTOM_TORRC_PATH) and _already_has_controlport(CUSTOM_TORRC_PATH):
            _p(f"✓ ControlPort already in {CUSTOM_TORRC_PATH}", GREEN)
        else:
            full_torrc = (
                "# OnionClaw custom torrc\n"
                f"DataDirectory {CUSTOM_TORRC_DIR}\n"
                + TORRC_SNIPPET
            )
            with open(CUSTOM_TORRC_PATH, "w") as f:
                f.write(full_torrc)
            _p(f"✓ Written: {CUSTOM_TORRC_PATH}", GREEN)
            _p(f"\n  Also set in .env:")
            _p(f"    TOR_DATA_DIR={CUSTOM_TORRC_DIR}")
        # Set TOR_DATA_DIR in .env so sicry.py finds the cookie
        _patch_env("TOR_DATA_DIR", CUSTOM_TORRC_DIR)
        _p(f"✓ TOR_DATA_DIR={CUSTOM_TORRC_DIR} written to .env", GREEN)
    except Exception as e:
        _p(f"  Could not write custom torrc: {e}", RED)
        _p("  Manual setup required — see README.md#tor-control-port", YELLOW)


def _fix_cookie_auth():
    """AUTH-1: apply permanent cookie-auth fix — group membership + torrc + systemd drop-in.

    The Tor cookie file (/run/tor/control.authcookie) is recreated on every
    restart with permissions that only the root / debian-tor group can read.
    Three complementary fixes are applied here so the fix survives reboots:

      1.  Add user to debian-tor group   (usermod -aG debian-tor $USER)
      2.  Add CookieAuthFileGroupReadable 1 to the active torrc
      3.  Install a systemd drop-in that chmod g+r the cookie after Tor starts
    """
    import grp
    import getpass
    current_user = getpass.getuser()
    if current_user == "root":
        _p("  Running as root — cookie auth should already work.", GREEN)
        return

    # ── Check / apply group membership ───────────────────────────
    try:
        tor_gid = grp.getgrnam("debian-tor").gr_gid
        user_groups = [g.gr_name for g in grp.getgrall() if current_user in g.gr_mem]
        already_in_group = "debian-tor" in user_groups
    except KeyError:
        # No debian-tor group — different distro; skip
        _p("  ⚠  No debian-tor group found (non-Debian system).", YELLOW)
        _p("     Use password auth instead (FIX B above).", YELLOW)
        return

    if already_in_group:
        _p(f"✓ {current_user} is already in debian-tor group", GREEN)
    else:
        _p(f"\n  {current_user} is NOT in the debian-tor group.", YELLOW)
        if _yes(f"  Apply FIX A: run 'sudo usermod -aG debian-tor {current_user}' now?"):
            try:
                ret = subprocess.run(
                    ["sudo", "usermod", "-aG", "debian-tor", current_user],
                    capture_output=True, text=True,
                )
                if ret.returncode == 0:
                    _p(f"✓ Added {current_user} to debian-tor", GREEN)
                    _p("  ⚠  You must log out and back in (or run: newgrp debian-tor) for the group to take effect.", YELLOW)
                else:
                    _p(f"✗ usermod failed: {ret.stderr.strip()}", RED)
                    _p("  Try manually:  sudo usermod -aG debian-tor $USER", YELLOW)
            except FileNotFoundError:
                _p("✗ sudo not available — run manually:  sudo usermod -aG debian-tor $USER", RED)

    # ── Add CookieAuthFileGroupReadable 1 to the active torrc ─────
    for torrc_path in TORRC_CANDIDATE_PATHS:
        if not os.path.exists(torrc_path):
            continue
        try:
            with open(torrc_path, "r") as f:
                content = f.read()
            if "CookieAuthFileGroupReadable" in content:
                _p(f"✓ CookieAuthFileGroupReadable already in {torrc_path}", GREEN)
            else:
                with open(torrc_path, "a") as f:
                    f.write(TORRC_COOKIE_FIX)
                _p(f"✓ Added CookieAuthFileGroupReadable 1 to {torrc_path}", GREEN)
                _p("  Restart Tor for it to take effect.", YELLOW)
        except PermissionError:
            _p(f"  ⚠  Cannot write {torrc_path} (permission denied)", YELLOW)
            _p(f"     Run:  echo 'CookieAuthFileGroupReadable 1' | sudo tee -a {torrc_path}", YELLOW)
        except Exception as e:
            _p(f"  ⚠  Error patching torrc: {e}", YELLOW)
        break  # only patch the first found torrc

    # ── Install systemd drop-in as belt-and-suspenders ───────────
    if _yes("\n  Also install systemd drop-in (belt-and-suspenders: chmod g+r cookie after every Tor start)?"):
        try:
            os.makedirs(SYSTEMD_DROPIN_DIR, exist_ok=True)
            with open(SYSTEMD_DROPIN_PATH, "w") as f:
                f.write(SYSTEMD_DROPIN_CONTENT)
            subprocess.run(["sudo", "systemctl", "daemon-reload"],
                           capture_output=True, check=False)
            _p(f"✓ Systemd drop-in written to {SYSTEMD_DROPIN_PATH}", GREEN)
            _p("  After restarting Tor, the cookie will always be group-readable.", GREEN)
        except PermissionError:
            _p(f"  ⚠  Cannot write {SYSTEMD_DROPIN_PATH} — try with sudo:", YELLOW)
            _p(f"     sudo mkdir -p {SYSTEMD_DROPIN_DIR}", YELLOW)
            _p(f"     sudo tee {SYSTEMD_DROPIN_PATH} << 'EOF'", YELLOW)
            _p(SYSTEMD_DROPIN_CONTENT + "EOF", YELLOW)
            _p("     sudo systemctl daemon-reload && sudo systemctl restart tor", YELLOW)
        except Exception as e:
            _p(f"  ⚠  Drop-in install failed: {e}", YELLOW)


def _verify_controlport():
    _p("\nChecking ControlPort connectivity …")
    try:
        import socket
        s = socket.create_connection(("127.0.0.1", 9051), timeout=2)
        s.close()
        _p("✓ ControlPort 9051 is open", GREEN)
    except Exception:
        _p("✗ ControlPort 9051 not reachable yet", YELLOW)
        _p("  → Start/restart Tor with the updated torrc config above.", YELLOW)
        return
    # AUTH-1/AUTH-2: actually test authentication, not just the port
    _p("  Testing Tor authentication (stem) …")
    try:
        import stem
        from stem.control import Controller
        env_password = os.environ.get("TOR_CONTROL_PASSWORD", "")
        env_file = os.path.join(_DIR, ".env")
        if not env_password and os.path.exists(env_file):
            for line in open(env_file):
                if line.strip().startswith("TOR_CONTROL_PASSWORD="):
                    env_password = line.strip().split("=", 1)[1].strip().strip('"')
                    break
        with Controller.from_port(port=9051) as ctrl:
            if env_password:
                ctrl.authenticate(password=env_password)
                _p("✓ Auth OK   (password from TOR_CONTROL_PASSWORD)", GREEN)
            else:
                ctrl.authenticate()  # tries cookie + no-auth
                _p("✓ Auth OK   (cookie / no-auth)", GREEN)
    except ImportError:
        _p("  ⚠  stem not installed — skipping auth test (install with: pip install stem)", YELLOW)
    except Exception as auth_err:
        _p(f"✗ Auth failed: {auth_err}", RED)
        _p("""
  Authentication test failed. Three reliable fixes:

  FIX A — Add your user to the debian-tor group (cookie auth survives restarts):
    sudo usermod -aG debian-tor $USER
    newgrp debian-tor    # (or log out and back in)

  FIX B — Use password auth (most reliable — never breaks on restarts):
    1. Generate a hashed password:
         tor --hash-password 'YOUR_SECRET_PASS'
    2. Add to /etc/tor/torrc (or your custom torrc):
         HashedControlPassword <output from step 1>
    3. Add to .env in this directory:
         TOR_CONTROL_PASSWORD=YOUR_SECRET_PASS
    4. Restart Tor.

  FIX C — CookieAuthFileGroupReadable 1 (works with FIX A — permanent):
    Add to /etc/tor/torrc:
         CookieAuthFileGroupReadable 1
    Then restart Tor. The cookie stays group-readable across all restarts.
""", YELLOW)
        # AUTH-1: Offer to apply the permanent fix automatically
        if _yes("  Apply the permanent AUTH-1 fix now (FIX A + FIX C + systemd drop-in)?"):
            _fix_cookie_auth()



