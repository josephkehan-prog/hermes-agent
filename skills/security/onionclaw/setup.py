"""
OnionClaw — First-Run Setup Wizard
===================================
Creates .env from .env.example (if not present), prompts for LLM credentials,
and writes a minimal Tor torrc that enables the ControlPort needed for
renew_identity() (circuit rotation).

Usage:
  python3 setup.py
"""
import os
import sys
import shutil
import subprocess
import json
import urllib.request

_DIR   = os.path.dirname(os.path.abspath(__file__))
_ENV   = os.path.join(_DIR, ".env")
_EXAMPLE = os.path.join(_DIR, ".env.example")
# Also check parent dir for the Sicry repo copy
_ENV_PARENT   = os.path.join(_DIR, "..", ".env")
_EXAMPLE_PARENT = os.path.join(_DIR, "..", ".env.example")

TORRC_SNIPPET = """\
# ── OnionClaw required settings ──────────────────────────────────
ControlPort 9051
CookieAuthentication 1
# CookieAuthFileGroupReadable 1  ← uncomment + add user to debian-tor group
#                                   for permanent cookie auth across restarts
# DataDirectory /tmp/tor_data   # uncomment to use a custom data dir
# ─────────────────────────────────────────────────────────────────
"""

# AUTH-1: torrc snippet that permanently fixes cookie permissions
TORRC_COOKIE_FIX = "CookieAuthFileGroupReadable 1\n"

# Systemd drop-in path for Tor (used as fallback when torrc can't be written)
SYSTEMD_DROPIN_DIR  = "/etc/systemd/system/tor.service.d"
SYSTEMD_DROPIN_PATH = os.path.join(SYSTEMD_DROPIN_DIR, "onionclaw-cookie.conf")
SYSTEMD_DROPIN_CONTENT = """\
# ── OnionClaw AUTH-1 fix ─────────────────────────────────────────────────────
# Makes the Tor cookie file group-readable so users in the debian-tor group
# can authenticate to the ControlPort without needing root.
# Generated by:  python3 setup.py
# ─────────────────────────────────────────────────────────────────────────────
[Service]
ExecStartPost=+/bin/sh -c 'chmod g+r /run/tor/control.authcookie 2>/dev/null; chmod g+r /var/lib/tor/control_auth_cookie 2>/dev/null; true'
"""


TORRC_CANDIDATE_PATHS = [
    "/etc/tor/torrc",
    "/usr/local/etc/tor/torrc",
    os.path.expanduser("~/.torrc"),
]

# Custom torrc written to /tmp/tor_data/torrc when system torrc is unwritable
CUSTOM_TORRC_DIR  = "/tmp/tor_data"
CUSTOM_TORRC_PATH = os.path.join(CUSTOM_TORRC_DIR, "torrc")

BOLD  = "\033[1m"
GREEN = "\033[32m"
YELLOW= "\033[33m"
RED   = "\033[31m"
CYAN  = "\033[36m"
RESET = "\033[0m"


def _p(msg, color=""):
    print(f"{color}{msg}{RESET}" if color else msg)


def _ask(prompt, default=""):
    try:
        val = input(f"{CYAN}{prompt}{RESET} [{default}]: ").strip()
        return val if val else default
    except (KeyboardInterrupt, EOFError):
        print()
        sys.exit(0)


def _yes(prompt):
    try:
        ans = input(f"{CYAN}{prompt}{RESET} [Y/n]: ").strip().lower()
        return ans in ("", "y", "yes")
    except (KeyboardInterrupt, EOFError):
        print()
        sys.exit(0)


